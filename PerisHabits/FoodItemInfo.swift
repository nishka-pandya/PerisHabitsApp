import Foundation

class FoodItemInfo{
    
    var name: String
    var expirationDate: Date
    var quantity: Int
    var location: String
    
    init(name: String, expirationDate: Date, quantity: Int, location: String){
        self.name = name
        self.expirationDate = expirationDate
        self.quantity = quantity
        self.location = location
    }
    
    
    
    
    
    
}
